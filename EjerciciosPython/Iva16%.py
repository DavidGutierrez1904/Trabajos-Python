#Tienda iva del 16%
print(" Este programa calcula el 16% del iva de un producto ")
P =  float(input(" Ingrese el primer precio del producto "))
P2 = float(input(" Ingrese el segundo precio del producto "))
T = (P*0.16)
T2 = (P2*0.16)
R = (P+T),(P2+T2)
print (" El total a pagar con iva es:", T , T2, R,)